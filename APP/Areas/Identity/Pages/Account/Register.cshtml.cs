        private async Task CreateRoles()
        {
            foreach (var role in Enum.GetValues(typeof(Role)))
            {
                // Criando os níveis de acesso
                bool roleExists = await _roleManager.RoleExistsAsync(role.ToString());
                if (!roleExists)
                {
                    _logger.LogInformation("Criando os níveis de acesso.");
                    await _roleManager.CreateAsync(new IdentityRole(role.ToString()));
                }
            }
        }
        
        private IUserEmailStore<ApplicationUser> GetEmailStore()
        {
            if (!_userManager.SupportsUserEmail)
            {
                throw new NotSupportedException("A interface do usuário padrão requer um repositório de usuário com suporte por email.");
            }
            return (IUserEmailStore<ApplicationUser>)_userStore;
        }
